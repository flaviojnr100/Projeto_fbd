public class Destino extends Endereco {
    private String nome;

    public Destino(String nome,String estado, String rua, String bairro, String cidade) {
        super(estado, rua, bairro, cidade);
        this.nome = nome;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
    
}
