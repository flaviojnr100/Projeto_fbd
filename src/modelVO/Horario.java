package modelVO;

/**
 *
 * @author Flavio
 */
public class Horario {
    private int id;
    private String hora;
    private String data;

    public Horario(String hora, String data) {
        this.hora = hora;
        this.data = data;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
}
