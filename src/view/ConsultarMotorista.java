    @Override
    public void update(Observable o, Object arg) {
        if(o instanceof ControllerConsultarMotorista){
            ((ControllerConsultarMotorista) o).LimparDados();
            ((ControllerConsultarMotorista) o).ColocarDados();
        }else if(o instanceof ControllerConsultarMotorista && ((ControllerConsultarMotorista) o).getTela().getjMenuAtualizar().equals(arg)){
            ((ControllerConsultarMotorista) o).LimparDados();
            ((ControllerConsultarMotorista) o).ColocarDados();
        }
        if(o instanceof ControllerEditarMotorista){
           String [] ex = (String[])arg;
           for(int i=0;i<6;i++){
               jTableMotorista.getModel().setValueAt(ex[i], Integer.parseInt(ex[6]), i+1);
           }
        }else if(o instanceof ControllerConsultarMotorista && ((ControllerConsultarMotorista) o).getTela().getBtnBuscar().equals(arg)){
            ((ControllerConsultarMotorista) o).ColocarDados(((ControllerConsultarMotorista) o).getMotoristasLike());
        }
    }
